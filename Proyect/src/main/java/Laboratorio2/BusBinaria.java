    public static void burbuja(int v[], int tam)
    {
        int temp;
	for (int i = 0; i < tam - 1; i++)
	{
            for(int k = 0; k < tam -1; k++)
            {
                if(v[k] > v[k + 1])
                {
                    temp = v[k];
                    v[k] = v[k + 1];
                    v[k + 1] = temp;
                }
            }
        }
    }
    
    public void Bbinaria(int v[], int tam, int elemento){
        int inicio = 0, centro = 0, fin = (tam - 1);
        boolean band  = false;
        String s = "";
        while((inicio <= fin) && band == false)
        {
            centro = (int)(inicio + fin) / 2;
            if (elemento == v[centro])
            {
                band = true;
            }
            else
            {
                if (elemento < v[centro])
                {
                    fin--;
                }
                else
                    inicio++;
            }     
        }
        if (band == true)
        {
            s += centro;
            MosPos.setText(s);
        }
        else
            MosPos.setText("No existe");
    }

