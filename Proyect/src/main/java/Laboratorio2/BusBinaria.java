    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed

        try
        {
            int elemento = Integer.parseInt(Elementos.getText());

            if (i < arr.length)
            {
                arr[i] = elemento;
                i++;

                if (i == 1)
                {
                    elementosString.append(elemento);
                }
                else
                {
                    elementosString.append(", ").append(elemento);
                }

                ElementosIng.setText(elementosString.toString());

                Elementos.setText("");

                if (i == arr.length)
                {
                    //JOptionPane.showMessageDialog(null, "Arreglo Completado");
                    btnInsertar.setEnabled(false);
                    Elementos.setEnabled(false);
                    btnOrdenar.setEnabled(true);
                    ElementosIng.setEditable(false);
                }
            }
        }
        catch(NumberFormatException ex)
        {
            Elementos.setText("No valido");
            //JOptionPane.showMessageDialog(null, "Ingresa un elemento valido para el arreglo");
        }
    }//GEN-LAST:event_btnInsertarActionPerformed

