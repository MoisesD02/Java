    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed

        String palabra = ElementosIng.getText().trim();
        int lon = palabra.length();
        int i = 0;
        int contador = 0;

        if (palabra.isEmpty()){
        
            JOptionPane.showMessageDialog(null, "Por favor, ingresa una palabra.", "Error", JOptionPane.ERROR_MESSAGE);
            
    }//GEN-LAST:event_btnIngresarActionPerformed
        else
        {
            ElementosIng.setEditable(false);
            btnMostrar.setEnabled(true);
            sal += Espacio(palabra, lon, i, contador, sal);
    
        }
    }
    
    public static int Espacio(String cadena,int lon, int i, int contador, String sal) 
    {
        if(i < lon) 
	{
		
            if((cadena.charAt(i)) == (' ')) 
            {
                contador++;
            }
		
            return Espacio(cadena,lon,i+1, contador, sal);	
	}
		
        return contador;		
    }

