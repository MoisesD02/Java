    private void btnIngEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngEActionPerformed

        int tam = 10;
        String [] numbers = ElementosIng.getText().split(",");

        int array [] = new int[tam];

        try
        {
            if (numbers.length == tam)
            {
                for (int i = 0; i < 10; i++)
                {
                    array[i] = Integer.parseInt(numbers[i].trim());
                }

                btnMostrar.setEnabled(true);
                btnIngE.setEnabled(false);
                ElementosIng.setEditable(false);
                sal += multiplicar(array,tam);
                btnMostrar.setEnabled(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "El tamaÃ±o del arreglo es de 10 Elementos");
            }
        }
        catch (NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null, "No valido");
        }
    }//GEN-LAST:event_btnIngEActionPerformed

