    private void darDeAlta() {
    String nombre = txtNombre.getText().trim();
    String sexo = txtSexo.getText().trim();
    String edad = txtEdad.getText().trim();

    if (nombre.isEmpty() || sexo.isEmpty() || edad.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, rellene todos los campos.");
        return; 
    }
    
    if (sexo.equals("m")) {
        sexo = "Hombre";
    } else if (sexo.equals("f")) {
        sexo = "Mujer";
    } else if (!sexo.equalsIgnoreCase("Hombre") && !sexo.equalsIgnoreCase("Mujer")) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese 'Hombre', 'Mujer', 'M' o 'F' en el campo de sexo.");
        return;
    }

    try {
        int ed = Integer.parseInt(edad);  


        if (indiceActual < dep.length) {
            Departamento nuevoE = new Departamento(nombre, sexo, ed);
            dep[indiceActual] = nuevoE;  
            indiceActual++;  
            btnEmpleado.setEnabled(true);
            btnMedad.setEnabled(true);
            btnRegistros.setEnabled(true);
            JOptionPane.showMessageDialog(this, "Empleado dado de alta con √©xito.");
        } else {
            JOptionPane.showMessageDialog(this, "No hay espacio para m√°s empleados.");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese valores v√°lidos para la edad.");
    }

    limpiarCampos();
}
    
    private void darDeBaja() {
        String nombre = txtNombre.getText();
        boolean encontrado = false;
        
        if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese el nombre del empleado.");
        return;
    }

        for (int i = 0; i < indiceActual; i++) {
            if (dep[i].getNombre().equalsIgnoreCase(nombre)) {
                for (int j = i; j < indiceActual - 1; j++) {
                    dep[j] = dep[j + 1];
                }
                dep[indiceActual - 1] = null;
                indiceActual--;
                JOptionPane.showMessageDialog(this, "Empleado dado de baja con √©xito.");
                encontrado = true;
                break;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "Empleado no encontrado.");
        }

        limpiarCampos();
    }
    
    
    private void MostrarEmpleado() {
    String nombre = mNombre.getText().trim();

    if (nombre.isEmpty()) {
        taRegistro.setText("Por favor, ingrese el nombre del Empleado.");
        return; 
    }

    boolean encontrado = false;
    for (int i = 0; i < indiceActual; i++) {
        if (dep[i].getNombre().equalsIgnoreCase(nombre)) {
            taRegistro.setText("Nombre: " + dep[i].getNombre() +
                "\tSexo: " + dep[i].getSexo() +
                "\tEdad: " + dep[i].getEdad());
            encontrado = true;
            break;
        }
    }

    if (!encontrado) {
        taRegistro.setText("Empleado no encontrado.");
    }
}
    
    private void modificarEmpleado() {
    String nombre = mNombre.getText().trim();

    if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa el nombre del Empleado.");
        return;
    }

    String edadtxt = edadM  .getText().trim();

    if (edadtxt.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa la edad.");
        return;
    }

    try {
        int ed = Integer.parseInt(edadtxt);

        boolean encontrado = false;
        for (int i = 0; i < indiceActual; i++) {
            if (dep[i].getNombre().equalsIgnoreCase(nombre)) {  // Comparaci√≥n insensible a may√∫sculas/min√∫sculas
                dep[i].setEdad(ed);

                JOptionPane.showMessageDialog(this, "Edad modificada.");
                encontrado = true;
                break;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "Empleado no encontrado.");
        }
    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa valores v√°lidos parala edad.");
    }
}
    
    
    public void obtenerEmpleadosPorSexo(String sexoBuscado, JTextArea taRegistro) {
    if (sexoBuscado.equalsIgnoreCase("M")) {
        sexoBuscado = "Hombre";
    } else if (sexoBuscado.equalsIgnoreCase("F")) {
        sexoBuscado = "Mujer";
    }

    boolean hayEmpleados = false; 

    taRegistro.setText("");

    for (Departamento d : dep) {
        if (d != null && d.getSexo().equalsIgnoreCase(sexoBuscado)) {
            String sexoM = d.getSexo().equalsIgnoreCase("Hombre") ? "M" : "F";
            taRegistro.append("Nombre: " + d.getNombre() + ", Sexo: " + sexoM + ", Edad: " + d.getEdad() + "\n");
            hayEmpleados = true; 
        }
    }

    if (!hayEmpleados) {
        taRegistro.setText("No hay registros de empleados con sexo: " + sexoBuscado);
    }
}

