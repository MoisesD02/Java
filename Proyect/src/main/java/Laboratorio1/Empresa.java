    private void darDeAlta() {
    String nombre = txtNombre.getText().trim();
    String telefono = txtTelefono.getText().trim();
    String saldo = txtSaldo.getText().trim();

    if (nombre.isEmpty() || telefono.isEmpty() || saldo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, rellene todos los campos.");
        return;  
    }

    try {
        double sald = Double.parseDouble(saldo);  
        String morosoSeleccionado = btnMoroso.getSelectedItem().toString().trim();
        boolean esMoroso = morosoSeleccionado.equals("Moroso");


        if (indiceActual < empresa.length) {
            Cempresa nuevoR = new Cempresa(nombre, telefono, sald, esMoroso);
            empresa[indiceActual] = nuevoR;  
            indiceActual++; 
            btnCliente.setEnabled(true);
            btnRegistros.setEnabled(true);
            JOptionPane.showMessageDialog(this, "Cliente dado de alta con √©xito.");
        } else {
            JOptionPane.showMessageDialog(this, "No hay espacio para m√°s clientes.");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese valores v√°lidos para el saldo.");
    }

    limpiarCampos();
}
    
    private void darDeBaja() {
        String nombre = txtNombre.getText();
        boolean encontrado = false;
        
        if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese el nombre del Cliente.");
        return;
    }

        for (int i = 0; i < indiceActual; i++) {
            if (empresa[i].getNombre().equalsIgnoreCase(nombre)) {
                for (int j = i; j < indiceActual - 1; j++) {
                    empresa[j] = empresa[j + 1];
                }
                empresa[indiceActual - 1] = null;
                indiceActual--;
                JOptionPane.showMessageDialog(this, "Cliente dado de baja con √©xito.");
                encontrado = true;
                break;
            }
        }

        if (!encontrado) {
            JOptionPane.showMessageDialog(this, "Cliente no encontrado.");
        }

        limpiarCampos();
    }
    
    
    private void MostrarCliente() {
    String nombre = mNombre.getText().trim();

    if (nombre.isEmpty()) {
        taRegistro.setText("Por favor, ingrese el nombre del Cliente.");
        return; 
    }

    boolean encontrado = false;
    for (int i = 0; i < indiceActual; i++) {
        if (empresa[i].getNombre().equalsIgnoreCase(nombre)) {
            taRegistro.setText("Nombre: " + empresa[i].getNombre() +
                "\tTelefono: " + empresa[i].getTelefono() +
                "\tSaldo: " + empresa[i].getSaldo() +
                "\tEstado: " + empresa[i].getMoroso());
            encontrado = true;
            break;
        }
    }

    if (!encontrado) {
        taRegistro.setText("Cliente no encontrado.");
    }
}
    
    
    private void actualizarEstadoMoroso() {
    String nombre = mNombre.getText().trim();

    if (nombre.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa el nombre del Empleado.");
        return;
    }

    String morosoSeleccionado = btnMoroso.getSelectedItem().toString().trim();

    if (morosoSeleccionado.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, selecciona si el empleado es moroso o no.");
        return;
    }

    boolean esMoroso = morosoSeleccionado.equals("Moroso");

    boolean encontrado = false;
    for (int i = 0; i < indiceActual; i++) {
        if (empresa[i].getNombre().equalsIgnoreCase(nombre)) {  
            empresa[i].setMoroso(esMoroso); 

            JOptionPane.showMessageDialog(this, "Estado de moroso actualizado.");
            encontrado = true;
            break;
        }
    }

    if (!encontrado) {
        JOptionPane.showMessageDialog(this, "Empleado no encontrado.");
    }
}


    
    private void imprimirRegistros() {
        taRegistro.setText("");
        if (indiceActual == 0) {
            taRegistro.append("No hay registros de Clientes.");
        } else {
            taRegistro.setText("Nombre\tTelefono\tSaldo\tEstadoMoroso\n");
            for (int i = 0; i < indiceActual; i++) {
                taRegistro.append(empresa[i].toString() + "\n");
            }
        }
    }
    
    private void limpiarCampos() {
        txtNombre.setText("");
        txtTelefono.setText("");
        txtSaldo.setText("");
    }

