    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String input = ListaO.getText();
        String[] numbers = input.split(",");
        String Alista = "";

        ArrayList<Integer> lista = new ArrayList<Integer>();

        for (String number : numbers) {
            try {
                int num = Integer.parseInt(number.trim());
                lista.add(num);
                Alista = "" + lista;
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Por favor, ingresa solo n√∫meros enteros separados por coma.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        Original.setText(Alista);
        Sacudida(lista);

        String sal = "";
        for (int num : lista) {
            sal += num + " ";
        }
        Ordenada.setText(sal.trim());
    }//GEN-LAST:event_jButton1ActionPerformed

    private void Sacudida(ArrayList<Integer> lista) {
        int n = lista.size();
        int temp;
        int izq = 1, der = n - 1, k = n - 1;

        while (der >= izq) {
            for (int j = der; j >= izq; j--) {
                if (lista.get(j - 1) > lista.get(j)) {
                    temp = lista.get(j - 1);
                    lista.set(j - 1, lista.get(j));
                    lista.set(j, temp);
                    k = j;
                }
            }
            izq = k + 1;

            for (int j = izq; j <= der; j++) {
                if (lista.get(j - 1) > lista.get(j)) {
                    temp = lista.get(j - 1);
                    lista.set(j - 1, lista.get(j));
                    lista.set(j, temp);
                    k = j;
                }
            }
            der = k - 1;
        }
    }

