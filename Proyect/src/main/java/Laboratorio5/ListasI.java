    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        String input = ListaO.getText();
        String[] numbers = input.split(",");
        String Apositivos = "";
        String Anegativos = "";

        ArrayList<Integer> positivos = new ArrayList<Integer>();
        ArrayList<Integer> negativos = new ArrayList<Integer>();

        for (String number : numbers) {
            try {
                int num = Integer.parseInt(number.trim());
                Mpositivos.setEnabled(true);
                Mnegativos.setEnabled(true);
                Mpositivos.setEditable(false);
                Mnegativos.setEditable(false);
                if (num >= 0) {
                    positivos.add(num);
                    Apositivos = ""+ positivos;
                } else {
                    negativos.add(num);
                    Anegativos = ""+ negativos;
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Por favor, ingresa solo n√∫meros enteros separados por coma.", "Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        
        Mpositivos.setText(Apositivos);
        Mnegativos.setText(Anegativos);
    
    }//GEN-LAST:event_jButton1ActionPerformed


