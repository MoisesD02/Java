    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String input = ListaO.getText();
        String[] numbers = input.split(",");
        String Amayores = "";
        String Amenores = "";

        ArrayList<Integer> mayores = new ArrayList<Integer>();
        ArrayList<Integer> menores = new ArrayList<Integer>();

        for (String number : numbers) {
            try {
                int num = Integer.parseInt(number.trim());
                Mmenores.setEnabled(true);
                Mmayores.setEnabled(true);
                Mmenores.setEditable(false);
                Mmayores.setEditable(false);
                if (num >= 0)
                {
                    if (num >= 10)
                    {
                        mayores.add(num);
                        Amayores = ""+ mayores;
                    }
                    else
                    {
                        menores.add(num);
                        Amenores = ""+ menores;
                    }
                }
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "Por favor, ingresa solo n√∫meros enteros separados por coma.", "Error",JOptionPane.ERROR_MESSAGE);
                return;
            }
        }

        Mmenores.setText(Amayores);
        Mmayores.setText(Amenores);
    }//GEN-LAST:event_jButton1ActionPerformed

